package sortit;


public class Util {

    /* A utility function to print array of size n */
    public static void print(int inpArray[]) {
        for (int i = 0; i < inpArray.length; ++i) {
            System.out.print(inpArray[i] + " ");
        }
        System.out.println();
    }
    
}
